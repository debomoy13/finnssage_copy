// Helper function to generate realistic stock data
function generateStockData(
    symbol: string,
    name: string,
    basePrice: number,
    sector: string,
    marketCapTrillion: number,
    peRatio: number
) {
    const variation = (Math.random() - 0.5) * 0.02; // ±1% variation
    const price = basePrice * (1 + variation);
    const change = basePrice * variation;
    const changePercent = variation * 100;

    const eps = price / peRatio;
    const dividend = price * (0.01 + Math.random() * 0.03); // 1-4% yield
    const high52w = price * (1.1 + Math.random() * 0.2); // 10-30% higher
    const low52w = price * (0.7 + Math.random() * 0.2); // 20-40% lower
    const avgVolume = (Math.random() * 20 + 1).toFixed(1); // 1-21M
    const beta = (0.8 + Math.random() * 0.8).toFixed(2); // 0.8-1.6

    return {
        name,
        price,
        change,
        changePercent,
        sector,
        marketCap: `₹${marketCapTrillion.toFixed(2)}T`,
        peRatio: peRatio.toFixed(2),
        eps: eps.toFixed(2),
        dividend: dividend.toFixed(2),
        high52w: high52w.toFixed(2),
        low52w: low52w.toFixed(2),
        avgVolume: `${avgVolume}M`,
        beta
    };
}

